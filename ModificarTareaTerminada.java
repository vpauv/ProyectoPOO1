    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        Frm_user ventana = new Frm_user();
        ventana.usrActual = usrActual;
        TareaN tarea;
        tarea = ventana.usrActual.tareasTerminadas.get(ventana.pos);
        ventana.usrActual.tareasTerminadas.remove(ventana.pos);
        tarea.cambiarAterminada();
        ventana.usrActual.tareasEntregadas.add(tarea);
        ventana.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_btnAceptarActionPerformed

